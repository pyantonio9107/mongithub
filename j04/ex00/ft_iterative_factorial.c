int		ft_iterativ_factorial(int nb)
{
	int	a;

	a = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		return (1);
	while (nb)
	{
		a = a * nb;
		nb--;
	}
	return (a);
}
